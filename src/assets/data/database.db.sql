BEGIN TRANSACTION;
DROP TABLE IF EXISTS `hero`;
CREATE TABLE IF NOT EXISTS `hero` (
	`id`	INTEGER NOT NULL,
	`name`	TEXT NOT NULL,
	PRIMARY KEY(`id`)
);
CREATE TABLE IF NOT EXISTS `customer` (
    `NO` 	INTEGER NOT NULL,
	`Party`	TEXT NOT NULL,
	`Address`	TEXT NOT NULL,
	`GSTNo` TEXT NOT NULL,
	`City` TEXT,
	`State` TEXT,
	PRIMARY KEY(`NO`)
);
COMMIT;
